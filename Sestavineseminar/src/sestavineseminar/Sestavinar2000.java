    private void listSeznamObrokovMValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listSeznamObrokovMValueChanged
        String izbira = listSeznamObrokovM.getSelectedValue();
        if (izbira != null) {

            ArrayList<String> seznamSestavin = new ArrayList();
            recept rec = new recept();
            int id = id = Integer.parseInt(izbira.substring(0, izbira.indexOf(",")));
            rec = implRec.getReceptByID(id);
            txtID.setText(String.valueOf(rec.getId()));
            txtImeP.setText(rec.getNaziv());
            txtOpisP.setText(rec.getOpis());
            txtStSes.setText(String.valueOf(rec.getStSestavin()));

            seznamSestavin = implRec.getAllSestavineRecepta(id);
            seznamSestavinRecepta.clear();
            seznamSestavin.forEach((action) -> seznamSestavinRecepta.addElement(action));
        } else {
            txtID.setText("");
            txtImeP.setText("");
            txtOpisP.setText("");
            txtStSes.setText("");
        }
    }//GEN-LAST:event_listSeznamObrokovMValueChanged

