    private void btndodajReceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndodajReceptActionPerformed
        recept rec = new recept();
        String naziv, nazivSes, opis;
        int stSestavin, id;
        ArrayList<sestavina> sestavin = new ArrayList();
        naziv = txtDodajNaziv.getText();
        opis = txtDodajOpis.getText();
        stSestavin = Integer.parseInt(txtDodajStSes.getText());
        for (String a : listDodajSestavine.getSelectedValuesList()) {
            sestavina sestavina1 = new sestavina();
            id = Integer.parseInt(a.substring(0, a.indexOf(",")));
            nazivSes = a.substring(a.indexOf(",") + 1);
            sestavina1.setId(id);
            sestavina1.setNaziv(nazivSes);
            sestavin.add(sestavina1);

        }
        rec.setNaziv(naziv);
        rec.setOpis(opis);
        rec.setStSestavin(stSestavin);

        implRec.insertRecept(rec, sestavin);

        recepti = implRec.getAllRecept();
        napolniSeznamReceptov(seznamObrokovM, recepti);
    }//GEN-LAST:event_btndodajReceptActionPerformed

    private void btnPreveriSestavineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreveriSestavineActionPerformed
        if (!listSeznamSestavinM.getSelectedValuesList().isEmpty()) {
            int id;
            String naziv;

            ArrayList<sestavina> sestavin = new ArrayList();
            for (String a : listSeznamSestavinM.getSelectedValuesList()) {
                sestavina sestavina1 = new sestavina();
                id = Integer.parseInt(a.substring(0, a.indexOf(",")));
                naziv = a.substring(a.indexOf(",") + 1);
                sestavina1.setId(id);
                sestavina1.setNaziv(naziv);
                sestavin.add(sestavina1);

            }
            recepti = implRec.getreceptBasedOnGiven(sestavin);
            napolniSeznamReceptov(seznamObrokovM, recepti);
        }
    }//GEN-LAST:event_btnPreveriSestavineActionPerformed

