    public void napolniSeznamSestavin(DefaultListModel model) {
        model.clear();
        sestavine = implSes.getAllsestavine();
        for (sestavina ses : sestavine) {

            model.addElement(ses.getId() + "," + ses.getNaziv());
        }
    }

    public void napolniSeznamReceptov(DefaultListModel modelReceptov, ArrayList<recept> recepti) {
        modelReceptov.clear();
        for (recept rec : recepti) {
            modelReceptov.addElement(rec.getId() + ", Naziv: " + rec.getNaziv() + ", Stevilo Sestavin: " + rec.getStSestavin());
        }
    }

